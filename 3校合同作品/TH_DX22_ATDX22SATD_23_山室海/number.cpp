void DrawNumber(int n, float x, float y)	// スケールによって座標位置が変わる　ｙ
{
	//if( n < 0 || n > 9) return;
	y = y - 2 * ((n-1) / 5);
	SetPolygonTexture(0);
	SetPolygonScale(0.5f,0.5f,0.0f,0.0f);
	PolygonDraw( x, y, (int)NUMBER_WIDTH * ((n-1) % 5), (int)NUMBER_HEIGHT * ((n-1) / 5), (int)NUMBER_WIDTH, (int)NUMBER_HEIGHT, true);
	// y - 4 * (n / 5) は画像の読み込みでずれるため
	//     ↑この数字はスケールによって変わる　等倍は４
}

